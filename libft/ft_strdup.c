#include "libft.h"

char	*ft_strdup(const char *s)
{
	unsigned int	len;
	char			*res;
	int				i;

	len = (unsigned int)ft_strlen(s);
	i = 0;
	res = (char *)malloc(len * sizeof(char) + 1);
	if (res == 0)
		return (0);
	while (s[i] != '\0')
	{
		res[i] = s[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
